function t(key, language = null) {
  const lang = language || localStorage.getItem('language') || 'en';
  
  if (LANGUAGE_OVERRIDES[lang] && LANGUAGE_OVERRIDES[lang][key]) {
    return LANGUAGE_OVERRIDES[lang][key];
  }
  
  return COMMON_PHRASES_EN[key] || key;
}

function getCurrentLanguage() {
  return localStorage.getItem('language') || 'en';
}

function setLanguage(langCode) {
  if (SUPPORTED_LANGUAGES[langCode]) {
    localStorage.setItem('language', langCode);
    return true;
  }
  return false;
}

function getSupportedLanguages() {
  return SUPPORTED_LANGUAGES;
}

